from pathlib import Path

BASE = Path("go-knowledge-base/01-Formal-Theory")

def write(path, content):
    full = BASE / path
    full.parent.mkdir(parents=True, exist_ok=True)
    with open(full, 'w', encoding='utf-8') as f:
        f.write(content)
    return full.stat().st_size

